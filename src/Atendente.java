
public class Atendente {
	private String cpf;
	private int slr;
	private String nome;
	public String getCpf() {
		return cpf;
	}
	public void setCpf(String cpf) {
		this.cpf = cpf;
	}
	public int getSlr() {
		return slr;
	}
	public void setSlr(int slr) {
		this.slr = slr;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
}
